# JSON
# 反序列化：由字符串到语言下某种数据结构的过程， 比如JSON object -> python dict, JSON array -> python list
# 序列化： 把python的数据类型向JSON字符串转化
# JSON里数据格式和Python里数据格式对照关系
# JSON        Python
# object      dict
# array       list
# string      str
# number      int
# number      float
# true        True
# false       False
# null        None
